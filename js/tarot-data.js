// 构建小阿卡纳牌
let minorId = 22;
SUITS.forEach(suit => {
  RANKS.forEach(rank => {
    const tKey = rank.rank <= 10
      ? ['ace','two','three','four','five','six','seven','eight','nine','ten'][rank.rank - 1]
      : ['page','knight','queen','king'][rank.rank - 11];
    const tmpl = MEANING_TEMPLATES[suit.suit][tKey];

    TAROT_DECK.push({
      id: minorId++,
      type: 'minor',
      suit: suit.suit,
      suitZh: suit.nameZh,
      element: suit.element,
      number: rank.rank,
      nameZh: `${suit.nameZh}${rank.nameZh}`,
      nameEn: `${rank.nameEn} of ${suit.nameZh}`,
      keywords: suit.keywords,
      meaning: tmpl.m,
      reversed: tmpl.r,
    });
  });
});


/**
 * 从塔罗牌堆中随机抽取 n 张不重复的牌
 * @param {number} n - 抽取数量，默认 3
 * @returns {Array} 抽取的牌数组
 */
function drawCards(n = 3) {
  const shuffled = [...TAROT_DECK].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}

